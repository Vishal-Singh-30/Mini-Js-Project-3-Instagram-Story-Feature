let stories = document.querySelector("#stories");

arr.forEach((element,index)=>{
    stories.innerHTML += `
    <div class="story">
        <img id="${index}" src="${element.dp}" alt="">
        <img src="${element.story}" alt="" class="story-img">
    </div>
    `
})

// as we dont the how many story cab be there so we will add 
// event listen to the stories panel insted of eah story element! 

let fullscreen = document.querySelector("#fullscreen");

stories.addEventListener("click",(e)=>{
    fullscreen.style.backgroundImage = `url(${arr[e.target.id].story})`;
    fullscreen.style.opacity = 1;
    fullscreen.style.visibility = "visible";

    setTimeout(() => {
        fullscreen.style.opacity = 0;
        fullscreen.style.visibility = "hidden";
    }, 3000);
});

